        private void save_month_cycle(DateTime startDate, DateTime endDate, string wq_input_path)
        {
            string outputPath = Path.Combine(wq_input_path, "month_cycle.txt");
            if (endDate < startDate)
                throw new ArgumentException("endDate must be greater than or equal to startDate");

            var lines = new List<string>
            {
                "#month_cycle: used for the output of monthly result"
            };

            int totalMonths = 0;
            int maxDaysInMonth = 31;

            DateTime current = new DateTime(startDate.Year, startDate.Month, 1);
            DateTime end = new DateTime(endDate.Year, endDate.Month, 1);

            // 先计算总月数
            while (current <= end)
            {
                totalMonths++;
                current = current.AddMonths(1);
            }

            lines.Add(string.Format("{0} {1} #num_month_cycle len_month_cycle", totalMonths, maxDaysInMonth));

            int dayCounter = 0;
            current = new DateTime(startDate.Year, startDate.Month, 1);

            while (current <= end)
            {
                int year = current.Year;
                int month = current.Month;
                int daysInMonth = DateTime.DaysInMonth(year, month);

                int startDay = dayCounter + 1;
                int endDay = dayCounter + daysInMonth;

                lines.Add(string.Format("{0} {1} 1 {2} {3} {4}", year, month, daysInMonth, startDay, endDay));

                dayCounter += daysInMonth;
                current = current.AddMonths(1);
            }

            File.WriteAllLines(outputPath, lines, Encoding.ASCII);
        }

        public void save_year_cycle(DateTime startDate, DateTime endDate, string wq_input_path)
        {
            string outputPath = Path.Combine(wq_input_path, "year_cycle.txt");
            if (endDate < startDate)
                throw new ArgumentException("endDate must be greater than or equal to startDate");

            var lines = new List<string>
            {
                "#year_cycle: used for the calculation of daily solar irradiation"
            };

            int yearCount = 0;
            int maxDaysInYear = 366;

            DateTime current = new DateTime(startDate.Year, 1, 1);
            DateTime endYear = new DateTime(endDate.Year, 1, 1);

            // 计算总年数
            while (current <= endYear)
            {
                yearCount++;
                current = current.AddYears(1);
            }

            lines.Add(string.Format("{0} {1} # num_year_cycle len_year_cycle", yearCount, maxDaysInYear));

            int dayCounter = 0;
            current = new DateTime(startDate.Year, 1, 1);

            while (current <= endYear)
            {
                int year = current.Year;
                bool isLeap = DateTime.IsLeapYear(year);
                int daysInYear = isLeap ? 366 : 365;

                int startDay = dayCounter + 1;
                int endDay = dayCounter + daysInYear;

                lines.Add(string.Format("{0} 1 {1} {2} {3}", year, daysInYear, startDay, endDay));

                dayCounter += daysInYear;
                current = current.AddYears(1);
            }

            File.WriteAllLines(outputPath, lines, Encoding.ASCII);
        }

