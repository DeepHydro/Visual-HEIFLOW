        private void save_nps_ex(string configpath, string wq_input_path, string fertfile)
        {
            var npsex = Path.Combine(configpath, "wq\\nps.ex");
            var npsex_out = Path.Combine(wq_input_path, "nps.ex");
            StreamReader sr = new StreamReader(npsex);
            StreamWriter sw = new StreamWriter(npsex_out);
            StreamReader sr_fert = new StreamReader(fertfile);
            string line = "";
            for (int i = 0; i < 38; i++)
            {
                line = sr.ReadLine();
                sw.WriteLine(line);
            }
            line = sr_fert.ReadLine();
            sw.WriteLine(line);

            var bufs = TypeConverterEx.Split<int>(line);
            int fert_times = bufs[0];
            int fert_hru = bufs[1];

            line = sr_fert.ReadLine();
            sw.WriteLine(line);
            line = sr_fert.ReadLine();
            sw.WriteLine(line);

            sr.Close();
            sr_fert.Close();
            sw.Close();

            var fertN = Path.Combine(wq_input_path, "ferti_N.txt");
            StreamWriter sw_fertN = new StreamWriter(fertN);
            line = string.Format("#fertilizer N file (kg/ha), according to num_fert_count and num_fert_cell in nps.ex. Generated by VHF on {0}", DateTime.Now);
            sw_fertN.WriteLine(line);
            float[] arr = Enumerable.Repeat(FertN, fert_hru).ToArray();
            var arr_str = string.Join(" ", arr);
            for (int i = 0; i < fert_times; i++)
            {
                sw_fertN.WriteLine(line);
            }
            sw_fertN.Close();

            var fertP = Path.Combine(wq_input_path, "ferti_P.txt");
            StreamWriter sw_fertP = new StreamWriter(fertN);
            line = string.Format("#fertilizer P file (kg/ha), according to num_fert_count and num_fert_cell in nps.ex. Generated by VHF on {0}", DateTime.Now);
            sw_fertN.WriteLine(line);
            arr = Enumerable.Repeat(FertP, fert_hru).ToArray();
            arr_str = string.Join(" ", arr);
            for (int i = 0; i < fert_times; i++)
            {
                sw_fertP.WriteLine(line);
            }
            sw_fertP.Close();

        }

